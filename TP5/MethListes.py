'''  - append() : ajoute un élément à la fin de la liste.
- insert() : insère un élément à une position donnée dans la liste.
- remove() : supprime la première occurrence d'un élément donné dans la liste.
- pop() : supprime et renvoie l'élément à une position donnée dans la liste, ou le dernier élément si aucune position n'est spécifiée.
- index() : renvoie la première position de l'élément donné dans la liste.
- count() : renvoie le nombre d'occurrences d'un élément donné dans la liste.
- sort() : trie les éléments de la liste dans l'ordre croissant.
- reverse() : inverse l'ordre des éléments dans la liste.

- extend() : ajoute les éléments d'une autre liste à la fin de la liste actuelle.
- clear() : supprime tous les éléments de la liste.
- copy() : renvoie une copie de la liste.
- len() : renvoie la longueur de la liste.
- max() : renvoie l'élément le plus grand de la liste.
- min() : renvoie l'élément le plus petit de la liste.
- sum() : renvoie la somme de tous les éléments de la liste.

- slice() : renvoie une partie de la liste, spécifiée par un début et une fin.
- join() : concatène les éléments d'une liste en une chaîne de caractères.
- map() : applique une fonction à chaque élément de la liste et renvoie une nouvelle liste avec les résultats.
- filter() : renvoie une nouvelle liste contenant les éléments pour lesquels une fonction donnée renvoie True.
- any() : renvoie True si au moins un élément de la liste est True, sinon renvoie False.
- all() : renvoie True si tous les éléments de la liste sont True, sinon renvoie False.

'''